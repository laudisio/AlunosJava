public class Aluno {

	private String nome;
	private int id;
	
	public Aluno(String nome, int id) {
		super();
		nome = nome;
		id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	
}
